// if else if statements
// giving remarks to a student based on his/her marks
void main() {
  var grade = 96;
  if (grade > 80) {
    print("Excellent JOB!!");
  } else if (grade > 60) {
    print("Good one");
  } else if (grade > 50) {
    print("above average");
  } else {
    print("Repeat the exam");
  }
}
